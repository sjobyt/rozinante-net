<p>Det var en trøndersk vinternatt i Januar 1980. Staale fant ut at det var på
tide å komme ut i den store verden. Etter tre år i Trondheim snakket
Staale flytende trøndersk, men da fant mamma og pappa ut at nå var det
på tide å flytte til Sør-Norge. Her ble han tatt vare på av ei snill dame
som kom fra Indre Agder. Med pappa fra Oslo, mamma fra Kristiansand
og mormor fra Dovrefjell, og morfar fra Telemark, ble det utviklet en ny
dialekt i kongeriket Norge.</p>

<p>Siste året før skolen bar det i barnehage, her var det mange
andre barn av begge kjønn. Jentene var de som var mest interessante.
Det førte til at de andre guttene mobbet ham, men det var bare å ta
igjen.</p>

<p>Første årene bodde familien på Fagerholt, men da det i 1990 ble
bestemt at nå skulle huset selges og båt kjøpes. flyttet familien til
Sodefjed. Sodefjed ligger helt i utkanten av Kristiansand kommune ved
Kvåsefjorden i Randesund ,ytterst i havgabet. Her vokste Staale opp
sammen med noen få andre mennesker på sin egen alder. I
sommerhalvåret ble tiden fylt med dykking, seiling og båtliv. I
vinterhalvåret ble tiden brukt til hyttebygging i skogen og datamaskiner.</p>

<p>Livet gikk sin gang og høsten 1995 begynte han å jobbe
med å reparere datamaskiner i en lokal databutikk. Mye av tiden ble
brukt til jobb og andre ting som unge voksne driver med. De første
årene før Staale ble for gammel og tøff var familien hver sommer på tur
i Rozinante. Turer til Danmark, Sverige, Shetland, Orkenøyene ble
gjennomført. Turen til Shetland ble tøff nok da det bare var tre personer
ombord. Staale var 13 år. Pappa måtte også ha søvn så Staale ble satt til
rors, med beskjed om å rope hvis det var noe som ikke stemte. Her ute i
Nordsjøen satt han alene godt bundet fast, og i dag er dette en
opplevelse Staale setter veldig stor pris på. Det var på denne turen
grunnlaget ble laget.</p>


<p>Etter at Staale hadde fullført et år på Videregående
Allmennfag, reiste pappa ut for å seile i et år, og det ville Staale være
med på. Dette var i 1997. Sommerjobben hos Hydralift ble straks utvidet
til å gjelde frem til Desember. I Desember reiste mamma, Staale og Stig,
lillebror, til Barbados for å møte pappa. Her var familien samlet for første
gang på lenge. Det var en opplevelse på både godt og vondt. Staale
hadde vokst fra familien som mange ungdommer gjør i denne alderen.
18 årsdagen ble feiret på sjøen mellom øyene St.Vincent og Saint Lucia.</p>

<p>I slutten av Januar reiste mamma og Stig hjem. Da kom kjæresten
Monica på besøk, og de hadde det veldig fint sammen. Da det nærmet
seg slutten på oppholdet til Monica, ble det vanskelig. Staale bestemte
seg, endret billett og reiste hjem sammen med Monica dagen etterpå.
Dette var kanskje ikke det lureste å gjøre, men Staale var ung og
forelsket, og da gjør man ofte dumme ting. Staale satt på flyet på vei
over Atlanterhavet og på Heathrow ringte han Hydralift og lurte på om
det var noen mulighet for en tidlig sommerjobb.
Sommerjobben ble på høsten utvidet til å være en
deltidsjobb.</p>

<p>Staale begynte i andre klasse på Videregående. Utover
vinteren og høsten ble deltidsjobben gjort om til fulltidsjobb og skolen ble
gjort om fra skole til deltidsskole. Vel og merke kun i hodet til Staale. For
jobben var fremdeles en deltidsjobb og skolen en fulltids skole. I praksis
ble det ikke slik. Staale var dobbelt så mye på jobb som på skolen.
De to neste årene var en blanding av jobb, skole og en stor
porsjon vasing,( sørlandsk for tull & tøys) Staale gikk ut av
videregående med det største fraværet i skolens historie.
Klasseforstanderen gjorde en kjempejobb på karaktermøtene, og
dermed ble det slik at Staale fikk karakter i alle fag utenom ett. Staale fikk
derfor ikke studiekompetanse og begynte å jobbe i et IT firma.</p>

<p>Når Staale fyller 23 får han studiekompetanse, da han har fem års arbeidserfaring,
men noen skolegang blir det ikke dette året heller. Sommeren 2000 var
Paul, Staale og Ragnar med flere på seiltur til Skagen med S/Y GUM.
Siden har det blitt mange turer til Skagen. Sommeren 2001 seilte vi
gjennom Limfjorden, sydover mot København og hjem til Norge igjen.
I løpet av denne turen bestemte Staale seg for å krysse Skagerrak fra
Kristiansand til Hirtshals i den 9 fots store VG jolla familien hadde hatt
siden 1986. Rozinante var med som følgebåt og turen var vellykket.
Etter 20 timer krabbet en utmattet Staale i land.</p>

<? insert_image("graphics/vg-jolla.jpg", "VG-jolla version 2", "pics/vg-jolla.jpg", "picleft", "", "") ?>

<p>Sommeren/Våren 2002 var preget av mange opplevelser på
godt og vondt. I Mai seilte Staale 1200 n.m. alene mellom Kristiansand,
Skagen, Gøteborg, Hanstholm og Hirtshals i en 33 fots seilbåt, GUM,
men VG jolla fikk også sin siste tur denne sommeren:
26. Juni 2002 la jeg ut i VG-Jolla i mye vind. VG-Jolla sprakk
etter en kullseiling og begynte å ta inn vann. Båten begynte å synke og
jeg skjønte at dette gikk den gale veien. Etter nøye vurdering av
situasjonen skjønte jeg at jeg trengte hjelp. Den medbrakte vanntette
VHFen var ikke lenger vanntett og derfor ubrukelig. Etter enda flere
vurderinger bestemte jeg meg for å sende opp nødraketter. Ventetiden
på havet ble lang. Takket være en tørrdrakt fikk jeg aldri panikk. Det ble
aldri kaldt, men de tre timene i vannet var en enorm psykisk påkjenning.
Tanker raser fort i slike situasjoner. For å la være å tenke , sang jeg høyt
for meg selv. Men tankene kom allikevel og mitt største ønske var å
kunne fortelle dem som betyr noe for meg at jeg hadde det bra. Følelsen
jeg fikk da jeg observerte et helikopter som fløy frem og tilbake over
havoverflaten kan vanskelig beskrives. Etter en halvtime med et
helikopter hengende over kom Losbåten fra Kristiansand og heiste meg
opp 10. nm ute i Skagerrak. To timer senere satt jeg sammen med gode
venner og feiret for meg selv at jeg ikke hadde omkommet der ute på
havet, for det hadde vært ganske så nære på. I ettertid vil jeg si at det
som skjedde, fikk meg til å sette veldig stor pris på det livet jeg har.
MEN: Ti dager etter ulykken, skjedde det en ny. </p>

