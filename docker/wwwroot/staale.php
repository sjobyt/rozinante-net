<p>Under Quart Festivalen, etter en fuktig kveld med konsert og en tur på byen
klatret Staale opp i et tre og datt ned. En ryggvirvel, (Lumbago virvel 1)
ble knust i tre biter. Legene åpnet opp, skrudde inn plater og bolter av
titan. Det var et stygt brudd og kunne gått mye verre. Staale kjempet
igjen, og 6 dager etter bruddet bar det ut med permisjon fra sykehuset
for å spise Pizza på Tømmerstø brygge. Sykehuset i Juli er definitivt
ikke stedet å være, men Staale ble der helt frem til August. Totalt ble det
4 uker på sykehus med fysioterapi. Etter hvert mente alle at nå var det
på tide å sende Staale hjem. Hydralift var igjen villig til å ta i mot en
skadet Staale. I løpet av høsten ble det lagt en plan i familien. En dag i
oktober ble avgjørelsen tatt. Staale skal ut på tur!
</p>


