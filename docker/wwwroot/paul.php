<p>Ragnar kom til verden 15. August 1980. To uker over
terminen slapp mamma Elin han endelig ut i vår lille verden.
Da Ragnar begynte på Samfundets skole i 1987, var han den
sterkeste i klassen! Klassen besto av Ragnar og hans gode
kamerat Thor M. 2 år gikk han på den lille skolen på
Dvergsnes, som bestod av 7 elever og 1 lærer. De neste 7
årene gikk han på Samfundets skole i Kristiansand. Siden
han var så slapp på skolen, fikk han elendige karakterer.
Karakterene kvalifiserte akkurat til å komme inn på
Grunnkurs mekaniske fag i 1996 (og det sier litt...). VK1 Plate
sveis gikk litt bedre, med 14 dager og 70 timer mindre fravær
enn førsteåret.</p>

<p>Ragnar begynte sommerjobb hos Aker Maritime
Promeco i 1998 og fikk lærlingplass som platearbeider. Han
jobbet der frem til Mars 2000, da reiste han til Værnes
rekruttskole for å starte sin militærtjeneste. Han søkte seg så
til Luftforsvarets skolesenter på Kjevik, hvor han lærte alt som
er vits å vite om våpensystemene ombord i jagerflyet F-16.</p>

<p>Etter endt kurs, sendte Uncle Sam Ragnar til Bodø
Hovedflystasjon for å se om han var brukende i praksis og.
Det var han nok, for Ragnar fikk to vinkler på skuldrene og
ble tillitsmann for soldatene i operasjonsgruppa. Ragnar likte
tjenesten så godt, at hadde Kjevik blitt en hovedflystasjon
skulle Ragnar vært førstemann til å søke ny jobb!! Ragnar
liker Sørlandet litt for godt til å flytte noe plass for en jobb.</p>

<p>Februar 2001 ble han dimmitert og sendt hjem til
vakre sør igjen. Han dreiv dank et par uker før han fikk jobb
hos Jens Knutsen mekaniske verksted og fikk ta fagbrev som
platearbeider med karakteren; Bestått meget godt. Der har
han jobbet siden og han trives både på jobb og hjemme, hvor
han bor på hybel sammen med kameraten kaptein Staale.
Da Ragnar var 7 år, flyttet han fra Tømmerstø til Søm. Han
har siden da syklet minst 600 ganger fram og tilbake mellom
Søm og Tømmerstø!</p>

<p>Ragnar Elsker Tømmerstø, spesielt om sommeren
når sjøen koker av småbåter og jenter i bikini. Båten hans er
ei 20 fots sjekte med 5-8 hk Marna motor. Den eier han
sammen med sin storebror Edvard. Hans første møte med
sjø på alle kanter var sommeren 2000. Seiltur til skagen med
Paul, Staale og et par andre kamerater.
Det var strålende sol hele veien, og Ragnar fant ut at
det jaggu ikke var så galt med seilbåt. Siden har det
blitt et par turer til, med Kuling og sjøsyke og med
masse godt humør. Han har lært et par nye ord
også, som klyver, storseil og fokk. Han har til
og med lært seg å bruke dem litt også!</p>

<p>Da Staale begynte å snakke om Langtur, var Ragnar
interessert med en gang. Og når det viste seg at det
kunne la seg gjøre, bestemte han seg !!
Ja... Jeg vil på tur!! Sa Ragnar...</p>

