<p>Allerede etter en vinter med intensivt arbeid (jeg
så på stort sett, men var flink til å holde med de andre med
selskap) så var en provisorisk Randø 29",GUM som den ble
hetende, (av mormors kjælenavn på mamma, Gull Ungen
Min) klar til å hives på vann. Randø 29" er en Colin Archer
kopi, og er av typen Breiflabb. Den var også blitt forlenget til
rett under 33 fot. For hvert år som gikk, ble båten nærmere
ferdig og stadig med nye duppedingser fra Reodor Felgens
verksted, les: pappa. En klar tendens var at ting gjort over
natten før tur, midlertidige ting, ble værende og mange er der
enda!</p>

<p>Før pappa reiste lovte han meg at hvis jeg tok båtførerbevis
og var ordentlig forsiktig, skulle jeg få låne båten mens han
var vekk. Det fikk jeg(faktisk!) tatt og som han lovte fikk jeg
ansvar for båten, med for og ulemper. Første år bar preg av
ungdommelige prioriteringer, hvor korte turer med høy musikk
og øl ble prioritert. Året etter ble det seilt mye mer som følge
av at det var Staale som var nestsjef(sjef?) ombord og ikke
min (nå X) kjæreste. Det ble gjennomført en svært vellykket
tur til København via Ålborg og innom Skagen på veien hjem.</p>

<p>Da var det gjort...! Nå blir det tur, og ikke en hvilken som helst
tur heller! Ser ut som jeg skal få utvidet min heller snevre
horisont ganske så mye. Blir vel rundt 20 forskjellige land vi
skal innom med dertil forskjellige opplevelser og ting å se!
Som personlige favoritter ser jeg vanvittig frem til klart blått,
og ikke minst VARMT hav å dykke i! For jeg skal, etter bare 5
år med somling og prating rundt emnet, få tatt dykkersertifikat
Dette blir bra tur!!!!
Og ellers er jeg en relativt likandes kar.. =)
</p>

