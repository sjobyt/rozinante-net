<p>Jeg født en tropesøndag i august i 1982 i Sørlandets hovedstad
Kristiansand. Nesten 4 år gammel flyttet jeg med mamma, pappa og
lillesøster til Trondheim og ble der til jeg skulle begynne på skolen, 7
år gammel. Med på lasset hjem til Tømmerstø var en ny lillebror og
en dialekt som var ødelagt for alltid.</p>

<p>Etter dette har jeg hatt bosted i Sørlandets perle nummer
1, Tømmerstø. Stedet som blir fornektet 9 av årets 12 måneder, men
som en ikke ubetydelig mengde mennesker setter stor pris på i de
deilige sommer månedene! Tømmerstø Brygge er jo verdenskjent
over hele Norge, og ikke få kjendiser er innom i løpet av sommeren,
enten fordi de har hytte her eller de stikker innom med båt.</p>

<p>I en alder av 20 år, bor jeg fortsatt hos mammaen min, en
grei ordning når jeg skal spare penger. (Hun har forresten meldt seg
på som bysse/dekkspike i Karibien, ymter stadig frampå om sin 25 år
gamle erfaring fra Atlanteren, men jeg er redd det blir mye
barnepike ut av det, så NEI takk mamma, ellers takk!). Penger ja, 4
somre hadde jeg jobb på Hydralift og var innom flere avdelinger, men
det siste skoleåret trengte jeg litt mer lommepenger og tok jobb som
telefonselger hos Current, for førerkort og båtførerbevis er jo som
kjent ikke gratis. I dag jobber jeg fulltid og vel så det på 1880
Nummeropplysningen, og har noe ekstrajobb i tillegg, Det er den
eneste måten det kan bli penger til tur.</p>

<p>Pappa flyttet for en 3 års tid siden et stykke unna,
nærmere bestemt til Shanghai-Kina. Og Sommeren 2000 ble det
faktisk også en 2 ukers tur til verdens mest folkerike land, i en av
verdens største byer Shanghai, på meg og mine søsken. Dette er
også mer eller mindre det jeg har sett av den store verden, ser man
bort fra diverse steder i våre nærmeste naboland, Danmark og
Sverige. (Ikke et vondt ord om dem!!)</p>

<p>Da jeg ble ferdig med 3 år ØK ADM på Gimle VGS bar det
rett i Forsvaret. Avdelingen som hadde meldt sitt ønske om å få ære
av å ha Menig Knudsen et år, var Hærens samband på Jørstadmoen
i Lillehammer. Her tjente jeg som Produktspesialist innen data , og
hadde en tjeneste som ikke kunne passet bedre! Året gikk fort og
planen om å ut å seile ble vel dannet et sted på veien... og grodde
helt av seg selv.</p>

<p>Mot slutten av verneplikten så det ikke ut som om det
skulle være mulig å få til en snarlig tur som var tenkt. Komme seg
avgårde før man grodde fast konseptet. Året i Forsvaret gikk
vanvittig fort! Da sommeren kom og jeg ble kastet ut av Forsvaret ,
hadde jeg ikke kommet lengre i tankegangen enn å jobbe som en
helt for å spare til tur. Første reserveplan som dannet seg, var at det
ikke skadet å ha penger hvis jeg skulle begynne å studere neste år
allikevel: Jobber dette året uansett jeg. Noe snakk om seiling ble det
innimellom men mange ting talte i mot. For å nevne noe; Staales
knekte rygg, mangel på penger og en eventuell båt, relativt viktige
ting vil mange si. Men Staale, som den brilliante mannen han er
mistet aldri målet av syne og sammen med sin far og sin mor formet
han en plan om at Staale skulle kjøpe seg inn i båten og låne den
ett år. Dette fortalte Staale som han gjør med alt nytt, helt uanmeldt
over telefonen en vanlig høstdag. Han var selvfølgelig også
helt tilfreds med svaret om at jeg VAR MED! Så derifra gikk
det veien... </p>

<p>Båtlivet var en naturlig del av oppveksten. Det
begynte med at familien hadde minst 14 dagers ferie med
farmor og farfars båt hver sommer, en "Sandvik 33". Vi hadde
også overtatt morfars gamle Askeladd med seil, så vi hadde
vært rundt på de fleste skjærene i vid omkrets. Familien
ønsket seg egen båt, og seilbåt måtte det være, må vite, med
soveplass til alle og dekksplass til hunden og så videre. Så
kvittet vi oss med campingvogna og Askeladden og kjøpte
båt(les: skrog) selv.</p>


