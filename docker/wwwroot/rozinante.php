<p>S/Y Rozinante er en 34 fots seilskøyte bygget ved Randesund
Båtbyggeri av Agnar Joachim Bang. Rozinante ble sjøsatt i 1984. I 1992 skiftet Rozinante eier. Rozinante eies nå av familien Jordan/Hornang.</p>
<p>Båten har to master, og er gaffelrigget. Seilgarderoben består av storseil, klyver, fokk, flygende klyver, toppseil og mesanseil. Noen av seilene er utslitt og må byttes før avreise. Tauverk, sjakler og resten av riggen vil bli nøye overhalt. Stormasten ble byttet på midten av 90-tallet.</p>

