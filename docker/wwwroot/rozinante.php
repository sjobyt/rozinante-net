<p>Skroget til Rozinante er av plast, men grunnet den spesielle formen var det ikke mulig å støpe båten i en form som er den vanlige metoden. Derfor ble skroget først bygget i 1/2 toms furu, deretter belagt med 3 mm plast, 1 cm isolerende materiale og tilslutt 5 mm plast.
Rozinante er ei skute bygget for havsseilas. Skuta er ingen skarpseiler, men tåler mye juling.</p>



<p>Rozinante er i meget god teknisk stand. Det spesielle forholdet mellom eiere og båt har ført til at Rozinante har fått en utmerket
omsorg opp gjennom årene. Vinteren og våren 2003 vil båten forberedes for turen. Skroget vil, under vannlinjen, bli pusset helt ned. Motoren av typen Kubota 24 hester dieselmotor, vil bli overhalt av en profesjonell mekaniker. Det elektriske annlegget vil også bli overhalt.</p>

<p>Treverket er lakket med en spesiell type elastisk lakk ved navn Coelan. Lakken har holdt seg utmerket. Enkelte steder må forsterkes, men generelt holder den meget bra. Til gjelgjeld koster denne lakken mye mer enn tradisjonell lakk.</p>

